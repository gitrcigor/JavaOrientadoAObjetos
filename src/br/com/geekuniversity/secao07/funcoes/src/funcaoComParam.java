// Chamada de um função simples com passagem de parâmetro

// Fonte:
// https://www.w3schools.com/java/java_methods_param.asp


public class funcaoComParam {

    static void exibirNomes(String pessoas) {

        System.out.println(pessoas);

    }

    public static void main(String[] args) {

        exibirNomes("Rita");
        exibirNomes("Rodrigo");
        exibirNomes("Ana");

    }

}
